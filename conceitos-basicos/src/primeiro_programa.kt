// todo programa inicia a partir da função main()
fun main() {
    println("Olá Mundo, esse é o meu primeiro programa em Kotlin!")
    var nome: String = "Gabriel Rodrigues dos Santos"
    println("Meu nome é: " + nome)
    /**
     * para criar uma constante em Kotlin,
     * utilizamos a palavra reservada val ao invés
     * de var
     */
    val constante: Int = 11
    println(constante)
    // constante = 100
    // o ponto e vírgula é opcional, é bom seguir o padrão de não utilizar
    println("Apresentando uma mensagem qualquer!");
}
